//Arithmetic Operators
let a = 20;
let b = 5;

console.log(`a = ${a}`)
console.log(`b = ${b}`)
console.log(`a + b = ${a+b}`)
console.log(`a - b = ${a-b}`)
console.log(`a * b = ${a*b}`)
console.log(`a / b = ${a/b}`)
console.log(`a % b = ${a%b}`)
console.log(`a ** b = ${a**b}`)

//Unary
console.log(`a++ = ${a++}`)
console.log(`b++ = ${b++}`)

//Comparision 
let age = 18;

console.log(`age = ${age}`)
console.log(`age > 18 : ${age>18}`)
console.log(`age >= 18 : ${age>=18}`)
console.log(`age > 18 : ${age>18}`)
console.log(`age < 18 : ${age<18}`)
console.log(`age <= 18 : ${age<=18}`)
console.log(`age != 18 : ${age!=18}`)
console.log(`age == 18 : ${age==18}`)
console.log(`age === 18 : ${age===18}`)
